package top.upstudy.pojo;

public class Student {
    private int id;
    private String name;
    private String major;

    /**
     * get field
     *
     * @return id
     */
    public int getId() {
        return this.id;
    }

    /**
     * set field
     *
     * @param id
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * get field
     *
     * @return name
     */
    public String getName() {
        return this.name;
    }

    /**
     * set field
     *
     * @param name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * get field
     *
     * @return major
     */
    public String getMajor() {
        return this.major;
    }

    /**
     * set field
     *
     * @param major
     */
    public void setMajor(String major) {
        this.major = major;
    }
}
